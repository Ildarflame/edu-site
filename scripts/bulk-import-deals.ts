async function getExistingDeals(): Promise<Set<string>> {
  const slugs = new Set<string>();
  let hasMore = true;
  let startCursor: string | undefined;

  while (hasMore) {
    const url = `https://api.notion.com/v1/databases/${databaseId}/query`;
    const body: any = { page_size: 100 };
    if (startCursor) body.start_cursor = startCursor;

    const res = await fetch(url, {
      method: "POST",
      headers: {
        Authorization: `Bearer ${process.env.NOTION_TOKEN}`,
        "Notion-Version": "2022-06-28",
        "Content-Type": "application/json",
      },
      body: JSON.stringify(body),
    });
    const response: any = await res.json();

    for (const page of response.results || []) {
      const slugProp = page.properties?.Slug?.rich_text;
      if (slugProp && slugProp.length > 0) {
        slugs.add(slugProp[0].plain_text);
      }
    }

    hasMore = response.has_more ?? false;
    startCursor = response.next_cursor ?? undefined;
  }

  return slugs;
}

async function importDeals() {
  console.log("Fetching existing deals...");
  const existingSlugs = await getExistingDeals();
  console.log(`Found ${existingSlugs.size} existing deals.`);

  const toImport = newDeals.filter((d) => !existingSlugs.has(d.slug));
  console.log(`Will import ${toImport.length} new deals (${newDeals.length - toImport.length} skipped as duplicates).`);

  let imported = 0;
  let failed = 0;

  for (const deal of toImport) {
    try {
      await notionCreatePage({
        parent: { database_id: databaseId },
        properties: {
          Name: { title: [{ text: { content: deal.name } }] },
          Slug: { rich_text: [{ text: { content: deal.slug } }] },
          Logo: { rich_text: [{ text: { content: deal.logo } }] },
          Category: { select: { name: deal.category } },
          Audiences: {
            multi_select: deal.audiences.map((a: string) => ({ name: a })),
          },
          Tagline: { rich_text: [{ text: { content: deal.tagline } }] },
          Description: {
            rich_text: [{ text: { content: deal.description } }],
          },
          Value: { rich_text: [{ text: { content: deal.value } }] },
          Steps: { rich_text: [{ text: { content: deal.steps } }] },
          URL: { url: deal.url },
          Featured: { checkbox: deal.featured },
        },
      });
      imported++;
      console.log(`  [${imported}/${toImport.length}] + ${deal.name}`);
    } catch (err: any) {
      failed++;
      console.error(`  FAILED: ${deal.name} — ${err.message}`);
    }

    // Rate limit: Notion allows ~3 requests/sec
    if (imported % 3 === 0) {
      await new Promise((r) => setTimeout(r, 350));
    }
  }

  console.log(`\nDone! Imported: ${imported}, Failed: ${failed}`);
}

importDeals().catch(console.error);
