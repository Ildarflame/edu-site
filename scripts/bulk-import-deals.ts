import * as fs from "fs";
import * as path from "path";

// Read .env.local directly to avoid shell parsing issues
const envPath = path.resolve(__dirname, "..", ".env.local");
const envContent = fs.readFileSync(envPath, "utf-8");
const envVars: Record<string, string> = {};
for (const line of envContent.split("\n")) {
  const match = line.match(/^([^=]+)=(.*)$/);
  if (match) envVars[match[1].trim()] = match[2].trim();
}

const NOTION_TOKEN = envVars.NOTION_TOKEN || process.env.NOTION_TOKEN!;
const databaseId = envVars.NOTION_DEALS_DATABASE_ID || process.env.NOTION_DEALS_DATABASE_ID!;

console.log(`Token starts with: ${NOTION_TOKEN.substring(0, 10)}...`);
console.log(`Database ID: ${databaseId}`);

const notionHeaders = {
  Authorization: `Bearer ${NOTION_TOKEN}`,
  "Notion-Version": "2022-06-28",
  "Content-Type": "application/json",
};

async function notionCreatePage(body: any): Promise<any> {
  const res = await fetch("https://api.notion.com/v1/pages", {
    method: "POST",
    headers: notionHeaders,
    body: JSON.stringify(body),
  });
  if (!res.ok) {
    const err = await res.json();
    throw new Error(err.message || `HTTP ${res.status}`);
  }
  return res.json();
}

